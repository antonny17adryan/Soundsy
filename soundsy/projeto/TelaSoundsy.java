        //Método para atualizar tabela
    public void atualizarTabela(){
        DefaultTableModel modelo = (DefaultTableModel) TbMusicas.getModel();//Criaçao de um modelo padrão de tabela
        modelo.setNumRows(0);
        
        Object linha[] = new Object[4];//Criação de um vetor de tamanho 6, por causa das colunas
        
        //for para percorrer a lista e atualizar a tabela
        for(int i = 0; i < listaMusicas.size(); i++){
            
            //Cada posição do array é a posição do objeto
            linha[0] = listaMusicas.get(i).getNome();//pega a posição da lista e cadastra o nome do usuario
            linha[1] = listaMusicas.get(i).getArtista();
            linha[2] = listaMusicas.get(i).getGenero();
            linha[3] = listaMusicas.get(i).getDuracao();
            
            modelo.addRow(linha);//adicionar novas linhas
            
        }
    }

