    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // Cadastra os dados que o usuário inseriu
        String nome = tfNome.getText();
        String artista = tfArtista.getText();
        String album = tfAlbum.getText();
        String duracao = tfDuracao.getText();
        
        if(nome.equals("")){
            JOptionPane.showMessageDialog(rootPane, "Preencha o espaço nome");
        }else if(artista.equals("")){
            JOptionPane.showMessageDialog(rootPane, "Preencha o espaço artista");
        }else if(album.equals("")){
            JOptionPane.showMessageDialog(rootPane, "Preencha o espaço album");
        }else if(duracao.equals("")){
            JOptionPane.showMessageDialog(rootPane, "Preencha o espaço duracao");
        }else{
            //Cria o Objeto Usuario
            Musicas musicas = new Musicas(nome, artista, nome, duracao);
            
            //Adiciona Objetos na lista de Obejtos
            listaMusicas.add(musicas);
            
            //exibe mensagem de sucesso
            JOptionPane.showMessageDialog(rootPane, "Sucesso");
            
            //chama o método atualizar tabela
            atualizarTabela();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

