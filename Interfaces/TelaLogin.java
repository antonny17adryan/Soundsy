    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed

        // Identificar clique no botão cadastrar
        
        String Nome = tfNome.getText();
        String Sexo = tfSexo.getText();
        String Login = tfLogin.getText();
        String Senha = pfSenha.getText();
        String SenhaConfirm = pfSenhaConf.getText();
        
        if(Nome.equals("")){
            JOptionPane.showMessageDialog(rootPane, "Preencha o Nome");
        }else if(Sexo.equals("")){
            JOptionPane.showMessageDialog(rootPane, "Preencha o Sexo");
        }else if(Senha.equals("")){
            JOptionPane.showMessageDialog(rootPane, "Preencha a Senha");
        }else if(!SenhaConfirm.equals(Senha)){
            JOptionPane.showMessageDialog(rootPane, "Confirme a Senha");
        }else{
            //Cria o Objeto Usuario
            Usuario usuario = new Usuario(Login, Senha, Nome, Sexo);
            
            //Adiciona Objetos na lista de Obejtos
            listaLogins.add(usuario);
            
            //chama o método atualizar tabela
            atualizarTabela();
            
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

