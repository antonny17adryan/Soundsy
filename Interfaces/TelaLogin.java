        public void atualizarTabela(){
        
        //Grava a lista de usuarios no arquivo
        try {
            GravadorLogin.gravar("listaLogin.dat", listaLogins);
        } catch (Exception e) {
        }
        //Toda vez que o atualizar tabela for chamado irá gravar o array list no arquivo
        
        DefaultTableModel modelo = (DefaultTableModel) TbUsuario.getModel();//Criaçao de um modelo padrão de tabela
        modelo.setNumRows(0);
        
        Object linha[] = new Object[2];//Criação de um vetor de tamanho 6, por causa das colunas
        
        //for para percorrer a lista e atualizar a tabela
        for(int i = 0; i < listaLogins.size(); i++){
            
            //Cada posição do array é a posição do objeto
            linha[0] = listaLogins.get(i).getNome();//pega a posição da lista e cadastra o nome do usuario
            linha[1] = listaLogins.get(i).getLogin();
            
            modelo.addRow(linha);//adicionar novas linhas
            
        }
    }

